/**
 * Pobiera stawkę WIBOR 3M obowiązującą na daną datę.
 * Szuka ostatniej stawki ustalonej przed podaną datą.
 */
export function getWiborRate(date: Date): number {
  const key = `${date.getFullYear()}-${String(date.getMonth() + 1).padStart(2, '0')}`;

  if (WIBOR_3M_RATES[key] !== undefined) {
    return WIBOR_3M_RATES[key];
  }

  // Szukaj ostatniej dostępnej stawki przed tą datą
  const sortedKeys = Object.keys(WIBOR_3M_RATES).sort();
  let lastRate = sortedKeys[0];

  for (const k of sortedKeys) {
    if (k <= key) {
      lastRate = k;
    } else {
      break;
    }
  }

  return WIBOR_3M_RATES[lastRate];
}

/**
 * Zwraca wszystkie dostępne daty stawek WIBOR
 */
export function getAvailableWiborDates(): string[] {
  return Object.keys(WIBOR_3M_RATES).sort();
}
